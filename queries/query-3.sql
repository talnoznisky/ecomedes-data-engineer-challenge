--
-- Get all users with all their invoices.
--
-- Output columns:
-- - user_id::INT
-- - user_name::TEXT
-- - invoice_id::INT
-- - invoice_name::TEXT
--

-- Write your query here:

SELECT NULL::INT AS user_id
     , NULL::TEXT AS user_name
     , NULL::INT AS invoice_id
     , NULL::TEXT AS invoice_name
 WHERE FALSE
